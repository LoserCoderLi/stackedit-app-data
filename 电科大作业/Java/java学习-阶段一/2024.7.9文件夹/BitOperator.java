public class BitOperator{

	public static void main(String[] args){

		//int 2 的原码：0 0000000 00000000 00000000 00000010 32位
		//		  补码：0 0000000 00000000 00000000 00000010 用于操作
		//int 3 的原码：0 0000000 00000000 00000000 00000011 32位
		//		  补码：0 0000000 00000000 00000000 00000011 用于操作
		
		//&（用补码操作）0 0000000 00000000 00000000 00000010 补码
		//		  原码：0 0000000 00000000 00000000 00000010 原码 转成十进制用于显示

		//|			    0 0000000 00000000 00000000 00000011
		//		  原码：0 0000000 00000000 00000000 00000011 转成十进制用于显示

		//^				0 0000000 00000000 00000000 00000001
		// 		  原码：0 0000000 00000000 00000000 00000001 转成十进制用于显示

		System.out.println(2&3); // 2
		System.out.println(2|3); // 3
		System.out.println(2^3); // 1


		//int -2的原码：1 0000000 00000000 00000000 00000010
		//		  补码：1 1111111 11111111 11111111 11111110
		//~（用补码操作）0 0000000 00000000 00000000 00000001
		//		  原码：0 0000000 00000000 00000000 00000001 转成十进制显示
		 System.out.println(~-2); // 1


	} 
}